package Capitulo_03;
public class Ex23 {
    public static void main(String[] args) {
        
        for(int i = 1; i <= 20; i++) {
            if(i % 2 == 0) {
                System.out.println(i);
            }
        }

        int n = 2;
        while(n <= 10) {
            System.out.println(n);
            n += 2;
        }
    }
}
