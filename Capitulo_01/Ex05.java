package Capitulo_01;
public class Ex05 {
    public static void main(String[] args) {
        int n1 = 20;
        int n2 = 15;

        // Adição:
        int soma = n1 + n2;
        System.err.println("Adição: " + n1 + " + " + n2 + " = " + soma);

        // Subtração: 
        soma = n1 - n2;
        System.err.println("Subtração: " + n1 + " - " + n2 + " = " + soma);

        // Multiplicação: 
        soma = n1 * n2;
        System.err.println("Multiplicação: " + n1 + " x " + n2 + " = " + soma);

        // Divisão: 
        soma = n1 / n2;
        System.err.println("Divisão: " + n1 + " / " + n2 + " = " + soma);

        // Módulo/Resto da divisão: 
        soma = n1 % n2;
        System.err.println("Módulo/Resto da divisão: " + n1 + " % " + n2 + " = " + soma);


    }
}
